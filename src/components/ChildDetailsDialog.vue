<script lang="ts">
  import type { ChildOutputDto } from '@/api/resolvers/child/dto/output/child-output.dto';
  import { type PropType } from 'vue';
  import { FormatManager } from '@/utils/FormatManager.ts';
  import { ChildResolver } from '@/api/resolvers/child/child.resolver.ts';
  import { useUserStore } from '@/stores/userStore';
  import { useAgeGroups } from '../shared/UseAgeGroups.ts';
  import type { DocsOutputFileUploadDto } from '@/api/resolvers/files/dto/output/docs-output-file-upload.dto';
  import apiConf from '@/api/api.conf.ts';
  import Button from 'primevue/button';
  import Dialog from 'primevue/dialog';

  export type ChildDetailsDialogData = {
    child: ChildOutputDto;
    childDocs: {
      birthFile: DocsOutputFileUploadDto,
      snilsFile: DocsOutputFileUploadDto,
      schoolFile: DocsOutputFileUploadDto,
      platformFile: DocsOutputFileUploadDto,
      consentFile: DocsOutputFileUploadDto,
    } | null,
    competencies: {
      id: number;
      name: string;
      description: string;
      expert: {
        lastName: string;
        firstName: string;
        patronymic: string;
      }
    }[]
  }

  export default {
    name: 'ChildDetailsDialog',
    components: {
      Button,
      Dialog
    },
    props: {
      childDetails: {
        type: Object as PropType<ChildDetailsDialogData | null>,
        default: null
      },
      showChildInfoProp: {
        type: Boolean,
        required: true,
      }
    },
    emits: [
      "show-mentors-list",
      "show-child-info",
      "edit-child",
      "update-toast-popup"
    ],
    data() {
      return {
        childResolver: new ChildResolver(),
        useAgeGroups: useAgeGroups,
        userStore: useUserStore(),
        showChildInfo: this.showChildInfoProp
      }
    },
    computed: {
      FormatManager() {
        return FormatManager
      }
    },
    watch: {
      showChildInfoProp() {
        this.showChildInfo = this.showChildInfoProp
      },
      showChildInfo(oldVal, newVal) {
        if (oldVal !== newVal)
          this.$emit("show-child-info", this.showChildInfo)
      }
    },
    methods: {
      async deleteChild(id: number) {
        if (confirm(
          `Вы уверены что хотите удалить ребенка '${this.childDetails?.child.firstName}'?`
        )) {
          const response = await this.childResolver.deleteById(id)
          if (response.status === 200) {
            await this.userStore.fillChildren()
            this.showChildInfo = false
          }
        }
      },
      checkDocument(doc: DocsOutputFileUploadDto) {
        if (!doc || !doc.id) {
          this.$emit("update-toast-popup", {
            title: 'Ошибка',
            message: 'Документ недоступен для просмотра'
          });
          return false;
        }
        return true
      },
      viewDocument(doc: DocsOutputFileUploadDto) {
        if (this.checkDocument(doc))
          window.open(`${apiConf.endpoint}/file-service/v1/files/view/${doc.id}`, '_blank');
      },

      downloadDocument(doc: DocsOutputFileUploadDto) {
        if (this.checkDocument(doc))
          window.location.href = `${apiConf.endpoint}/file-service/v1/files/download/${doc.id}`;
      }
    }
  };
</script>

